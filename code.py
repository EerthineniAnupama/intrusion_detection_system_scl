import pyswarms as ps

def evaluate_swarm(params):
    """
    This function runs on each 'particle'.
    params: [learning_rate, dropout_rate]
    """
    results = []
    for p in params:
        lr, dropout = p[0], p[1]

        # Define a simple NN
        model = nn.Sequential(
            nn.Linear(41, 64),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(64, 1),
            nn.Sigmoid()
        ).cuda()

        optimizer = torch.optim.Adam(model.parameters(), lr=lr)
        criterion = nn.BCELoss()

        # Small fast training loop for PSO evaluation
        model.train()
        for _ in range(10): # 10 iterations to test the 'strength' of these params
            optimizer.zero_grad()
            outputs = model(X_train_gpu)
            loss = criterion(outputs, y_train_gpu)
            loss.backward()
            optimizer.step()

        results.append(loss.item())
    return np.array(results)

# PSO Setup
options = {'c1': 0.5, 'c2': 0.3, 'w': 0.9}
# Search space: [Learning Rate (0.001 to 0.1), Dropout (0.1 to 0.5)]
constraints = (np.array([0.001, 0.1]), np.array([0.1, 0.5]))

optimizer = ps.single.GlobalBestPSO(n_particles=10, dimensions=2,
                                    options=options, bounds=constraints)

# Perform Optimization
best_cost, best_params = optimizer.optimize(evaluate_swarm, iters=15)
print(f"Best Params found: LR={best_params[0]}, Dropout={best_params[1]}")

